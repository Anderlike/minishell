#include "../../lib/minishell.h"

void    env(t_env *mini)
{
    int     i;
    char    **env;

    i = 0;
    env = mini->env;
    while (env[i])
    {
        write(1, env[i], ft_strlen(env[i]));
        write(1, "\n", 1);
        i++;
    }
}